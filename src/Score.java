
public class Score {
	
	private int betweennesScore;
	private int totalDistanceScore;
	
	public Score(int betweennesScore, int totalDistanceScore) {
		
		this.betweennesScore = betweennesScore;
		this.totalDistanceScore = totalDistanceScore;
	}

	public int getBetweennesScore() {
		return betweennesScore;
	}

	public void setBetweennesScore(int betweennesScore) {
		this.betweennesScore = betweennesScore;
	}

	public int getTotalDistanceScore() {
		return totalDistanceScore;
	}

	public void setTotalDistanceScore(int totalDistanceScore) {
		this.totalDistanceScore = totalDistanceScore;
	}
	
	
	
	
	
	
	
	

}
